// for loop

for(let i = 1; i<=10; ++i){
    console.log("Jatin Garg");
}

// While loop -- it is same as for loop

let i = 1;
while(i<=5){
    console.log("Jatin Garg");
    i++;
}

// do - while loop -- it rums at least one time

let a = 20;

do{
    console.log(a);
} while(a<=15);

// for-of loop -- it is used for string and array not for object

let str = "Javascript";

for(let val of str){
    console.log(val);
}